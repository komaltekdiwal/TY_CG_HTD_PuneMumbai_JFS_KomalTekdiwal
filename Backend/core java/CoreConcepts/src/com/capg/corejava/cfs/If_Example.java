package com.capg.corejava.cfs;

public class If_Example
{
	public static void main(String[] args)
	{
		int i=11;
		int j=5;
		
		if(i<10)                               //if-else statement
		{
			System.out.println("i is single digit");
		}
		else
		{
			System.out.println("i is not single digit");
		}
	    

		if(j<=10)                               //if-else statement
		{
			System.out.println("j is single digit");
		}
		else
		{
			System.out.println("jis not single digit");
		}
		
	
		System.out.println("Execution has completed");
	}
}
