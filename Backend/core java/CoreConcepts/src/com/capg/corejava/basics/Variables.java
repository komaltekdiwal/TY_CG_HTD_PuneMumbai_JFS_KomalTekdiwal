package com.capg.corejava.basics;

public class Variables 
{
	public static void main(String[] args)
	{
		byte b = 10;          //declaration and initialization of byte
		short s = 101;        //declaration and initialization of short
		int i = 11;           //declaration and initialization of integer
		float f = 101.01f;    //declaration and initialization of float
		long l = 123456789L;  // declaration and initialization of long
		double d = 123.1;     //declaration and initialization of double
		char c = 'a';         //declaration and initialization of char
		boolean q = true;     //declaration and initialization of boolean
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(l);
		System.out.println(d);
		System.out.println(c);
		System.out.println(q);
	}
}
