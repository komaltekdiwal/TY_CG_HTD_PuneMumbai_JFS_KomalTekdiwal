package com.capg.corejava.basics;

public class ArithmeticOperators
{
	public static void main(String[] args)
	{
		int a = 10;                   //declaration and initialization
		int b = 5;                    //declaration and initialization
		int c=a+b;                    // Addition 
		System.out.println("a+b = "+c);
		
		c = a-b;                     // Subtraction and re-initialization
		System.out.println("a-b = "+c);
		
		c = a*b;                     // Multiplication and re-initialization
		System.out.println("a*b = "+c);
		
		c = a/b;                     // Division and re-initialization
		System.out.println("a/b = "+c);
		
		c = a%b;                     // Modulus and re-initialization
		System.out.println("a/b = "+c);
		
	}
	
}
