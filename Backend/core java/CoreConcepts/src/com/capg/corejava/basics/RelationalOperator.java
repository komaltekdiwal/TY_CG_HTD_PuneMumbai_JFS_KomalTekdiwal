package com.capg.corejava.basics;

public class RelationalOperator
{
	public static void main(String[] args) 
	{
		int i = 10;
		int j=20;
		System.out.println(i==j);     //equals to operator
		System.out.println(i!=j);     //not equals to operator
		System.out.println(i>j);      //greater than operator
		System.out.println(i<j);      //less than operator
		System.out.println(i>=j);     // greater than equal to operator
		System.out.println(i<=j);     //less than equal to operator
	}
}
