package com.capg.corejava.loops;

public class WhileLoop 
{
	public static void main(String[] args) 
	{
		int i = 5;
		while(i<=20)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("Code outside while loop");
		
		
		/*
		 * while(true)
		 *  {
		 *   System.out.println(i);
		 *    i++;
		 *     }
		 * System.out.println("Code outside while loop");
		 */
	}
}
