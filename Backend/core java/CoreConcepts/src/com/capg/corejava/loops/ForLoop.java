package com.capg.corejava.loops;

public class ForLoop
{
	public static void main(String[] args)
	{
		int i = 5;
			if(i%2==0)
			{
				for(int j =1;j<=5;j++)
				{
					System.out.println("j is "+j);
				}
				
			}
			else
			{
				for(int k =1;k<5;k++)
				{
					System.out.println("k is "+k);
				}
				
			}
	}
			
}